#!/usr/bin/env python3
"""
dataset_discovery.py — Identify relevant datasets from paper abstracts,
classify by type, accessibility, repository source, and quality.

Outputs
-------
outputs/reports/available_datasets.csv
outputs/reports/dataset_landscape.md
"""

from __future__ import annotations

import argparse
import json
import logging
import re
from collections import defaultdict
from datetime import datetime
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import pandas as pd

logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(levelname)s] %(name)s: %(message)s")
log = logging.getLogger("dataset_discovery")

DATASET_PATTERNS = [
    r"(?:dataset|data set|data)\s+(?:is|was|are|were)\s+(?:available|accessible|obtained|collected|from|using)",
    r"(?:data from|data were|data was|survey data|panel data|administrative data)",
    r"(?:repository|database|registry|archive|catalogue|catalog)",
    r"(?:github\.com|zenodo|figshare|dryad|dataverse|osf\.io|kaggle)",
    r"(?:open.?access|publicly available|public domain|open data)",
]

REPO_PATTERNS = {
    "GitHub": r"github\.com[/:][\w-]+/[\w-]+",
    "Zenodo": r"zenodo\.org[/:][\w-]+",
    "Figshare": r"figshare\.com[/:][\w-]+",
    "Dryad": r"datadryad\.org[/:][\w-]+",
    "Dataverse": r"dataverse[\w]*\.[\w]+",
    "OSF": r"osf\.io[/:][\w-]+",
    "Kaggle": r"kaggle\.com[/:][\w-]+",
}

DATA_TYPES = {
    "survey": r"(?:survey|questionnaire|Likert|self.report)",
    "administrative": r"(?:administrative|registry|census|administrative record)",
    "qualitative": r"(?:interview|focus group|field note|observation|qualitative)",
    "experimental": r"(?:experiment|RCT|trial|laboratory|controlled)",
    "longitudinal": r"(?:longitudinal|panel|cohort|follow.up|repeated)",
    "geospatial": r"(?:GIS|remote sensing|spatial|satellite|geographic)",
    "text": r"(?:text|corpus|document|narrative|transcript)",
    "clinical": r"(?:clinical|patient|biomarker|medical record|health record)",
}


def discover_datasets(papers_path: str = "search_results.csv") -> pd.DataFrame:
    df = pd.read_csv(papers_path) if Path(papers_path).exists() else pd.DataFrame()
    if df.empty:
        return df

    rows = []
    for _, row in df.iterrows():
        title = str(row.get("title", ""))
        doi = str(row.get("doi", ""))
        abstract = str(row.get("abstract", ""))
        if not title or title.lower() in ("nan", "", "none"):
            continue

        text = f"{title} {abstract}"

        # Check for dataset mentions
        has_dataset = any(re.search(p, text, re.IGNORECASE) for p in DATASET_PATTERNS)
        if not has_dataset:
            continue

        # Identify repositories
        repos_found: List[str] = []
        for repo_name, pat in REPO_PATTERNS.items():
            if re.search(pat, text, re.IGNORECASE):
                repos_found.append(repo_name)
        repo = "; ".join(repos_found) if repos_found else "Not specified"

        # Determine accessibility
        if any(re.search(p, text, re.IGNORECASE) for p in [r"open.?access", r"publicly available", r"open data"]):
            accessibility = "Open Access"
        elif any(re.search(p, text, re.IGNORECASE) for p in [r"restricted", r"upon request", r"contact author"]):
            accessibility = "Restricted"
        else:
            accessibility = "Unknown"

        # Data types
        types_found: List[str] = []
        for dt_name, pat in DATA_TYPES.items():
            if re.search(pat, text, re.IGNORECASE):
                types_found.append(dt_name)
        data_type = "; ".join(types_found) if types_found else "Not specified"

        # Quality heuristic
        quality = "High"
        if accessibility == "Unknown":
            quality = "Medium"
        if not repos_found and accessibility != "Open Access":
            quality = "Low"

        rows.append({
            "paper_title": title[:120],
            "doi": doi,
            "repository": repo[:100],
            "accessibility": accessibility,
            "data_types": data_type[:100],
            "quality": quality,
        })

    return pd.DataFrame(rows)


def _generate_report(df: pd.DataFrame) -> str:
    lines: List[str] = []
    lines.append("# Dataset Landscape\n")
    lines.append(f"- **Datasets identified:** {len(df)}")
    lines.append(f"- **Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M')}\n")

    if df.empty:
        lines.append("No datasets discovered in the current corpus.\n")
        lines.append("---\n*Generated by dataset_discovery.py*")
        return "\n".join(lines)

    lines.append("## By Type\n")
    type_counts = defaultdict(int)
    for t in df["data_types"]:
        for dt in str(t).split("; "):
            if dt and dt != "Not specified":
                type_counts[dt] += 1
    for dt, c in sorted(type_counts.items(), key=lambda x: -x[1]):
        lines.append(f"- **{dt.capitalize()}:** {c}")

    lines.append("\n## By Accessibility\n")
    for acc in ["Open Access", "Restricted", "Unknown"]:
        c = len(df[df["accessibility"] == acc])
        if c:
            lines.append(f"- **{acc}:** {c}")

    lines.append("\n## By Quality\n")
    for q in ["High", "Medium", "Low"]:
        c = len(df[df["quality"] == q])
        if c:
            lines.append(f"- **{q}:** {c}")

    lines.append("\n## Available Datasets\n")
    for _, row in df.iterrows():
        lines.append(f"- **{row['paper_title'][:60]}**")
        lines.append(f"  - Repository: {row['repository']}")
        lines.append(f"  - Accessibility: {row['accessibility']}")
        lines.append(f"  - Type: {row['data_types']}")
        lines.append(f"  - Quality: {row['quality']}\n")

    lines.append("---\n*Generated by dataset_discovery.py*")
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Discover datasets from literature.")
    parser.add_argument("--papers", type=str, default="search_results.csv")
    parser.add_argument("--output-dir", type=str, default="outputs/reports")
    args = parser.parse_args()

    df = discover_datasets(args.papers)

    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    if not df.empty:
        df.to_csv(out_dir / "available_datasets.csv", index=False)
        log.info("Saved -> %s", out_dir / "available_datasets.csv")

    report = _generate_report(df)
    with open(out_dir / "dataset_landscape.md", "w") as f:
        f.write(report)
    log.info("Saved -> %s", out_dir / "dataset_landscape.md")

    print(f"\n--- Dataset Discovery Complete ---")
    print(f"  Datasets identified: {len(df)}")
    print()


if __name__ == "__main__":
    main()
